package com.heqingsong.Mediator;

public class User {
    String name;

    public User(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void showMessage(String msg) {
        ChatRoom.showMessage(this, msg);
    }
}
