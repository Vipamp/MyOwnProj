package com.heqingsong.Command;

public class Invoker {
    Command command;

    public Invoker() {
    }

    public Invoker(Command command) {
        this.command = command;
    }

    public Command getCommand() {
        return command;
    }

    public void setCommand(Command command) {
        this.command = command;
    }

    public void execute() {
        command.execute();
    }
}
